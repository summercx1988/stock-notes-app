const traditionalToSimplifiedMap: Record<string, string> = Object.fromEntries(traditionalToSimplifiedPairs)

const toHalfWidthChar = (char: string): string => {
  const code = char.charCodeAt(0)
  if (code === 12288) return ' '
  if (code >= 65281 && code <= 65374) {
    return String.fromCharCode(code - 65248)
  }
  return char
}

export const toHalfWidthText = (text: string): string => Array.from(text).map(toHalfWidthChar).join('')

export const removeTranscriptTimestamps = (text: string): string =>
  text.replace(TIMESTAMP_MARKER, ' ')

export const normalizeToSimplifiedChinese = (text: string): string =>
  Array.from(text).map((char) => traditionalToSimplifiedMap[char] || char).join('')

export const normalizeStockNameText = (text: string): string => {
  const halfWidth = toHalfWidthText(text || '')
  const simplified = normalizeToSimplifiedChinese(halfWidth)
  return simplified.replace(/\s+/g, '').trim()
}

export const cleanTranscriptText = (text: string): string => {
  const noTimestamp = removeTranscriptTimestamps(text || '')
  const halfWidth = toHalfWidthText(noTimestamp)
  const simplified = normalizeToSimplifiedChinese(halfWidth)
  return simplified
    .replace(/\s+/g, ' ')
    .replace(/\s+([,，。！？；：])/g, '$1')
    .trim()
}

export const normalizeNoteContent = (text: string): string => {
  const noTimestamp = removeTranscriptTimestamps(text || '')
  const halfWidth = toHalfWidthText(noTimestamp)
  const simplified = normalizeToSimplifiedChinese(halfWidth)
  return simplified
    .replace(/\r\n/g, '\n')
    .replace(/[ \t]+\n/g, '\n')
    .replace(/\n{3,}/g, '\n\n')
    .trim()
}
